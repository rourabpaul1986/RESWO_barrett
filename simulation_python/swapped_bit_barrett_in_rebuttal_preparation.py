def swap_bits(number, pos1, pos2):
    # Extract the bits at the given positions
    bit1 = (number >> pos1) & 1
    bit2 = (number >> pos2) & 1

    # If the bits are different, toggle them
    if bit1 != bit2:
        # Create a bitmask with 1s at pos1 and pos2
        bitmask = (1 << pos1) | (1 << pos2)
        # Toggle the bits using XOR
        number ^= bitmask

    return number

def get_ith_bit(number, i):   # Extract the i^th bit position (0-based)
   return (number >> i) & 1


